// Runtime-caching only (no precache list) since Next.js build output uses
// content-hashed filenames that change every deploy - a static asset list
// would go stale. This gives installability + a basic offline fallback for
// pages already visited; revisit for a fuller offline strategy in a later
// phase if needed.
const CACHE_NAME = "adaptive-coach-v2";

self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches.keys().then((keys) => Promise.all(keys.filter((k) => k !== CACHE_NAME).map((k) => caches.delete(k)))),
  );
  self.clients.claim();
});

self.addEventListener("fetch", (event) => {
  if (event.request.method !== "GET") return;

  event.respondWith(
    fetch(event.request)
      .then((response) => {
        const copy = response.clone();
        caches.open(CACHE_NAME).then((cache) => cache.put(event.request, copy));
        return response;
      })
      .catch(() => caches.match(event.request)),
  );
});
